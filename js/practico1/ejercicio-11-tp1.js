let numero1 = parseInt(prompt("Escriba un numero"));

if (numero1 / 2 && (numero1 % 2) == 0){
    document.write("El numero es divisible por 2")
}else if ((numero1 / 3 && (numero1 % 3) == 0)) {
    document.write("El numero es divisible por 3")
}else if (numero1 / 5 && (numero1 % 5) == 0) {
    document.write("El numero es divisible por 5")
}else if (numero1 / 7 && (numero1 % 7) == 0){
    document.write("El numero es divisible por 7")
}